package OOPs.CollectionFramework;

public class LinkedListDemo {

    private Node head;
    private Node tail;
    private int size;

    public LinkedListDemo() {
        this.size = 0;
    }

    public void insertFirst(int val) {
        Node node = new Node(val);
        node.next = head;
        head = node;

        if (tail == null) {
            tail = head;
        }
        size++;
    }

    public void insertLast(int val) {
        if (tail == null) {
            insertFirst(val);
            return;
        }
        Node node = new Node(val);
        tail.next = node;
        tail = node;
        size++;
    }

    public void insert(int val, int index) {
        if (index == 0) {
            insertFirst(val);
            return;
        }
        if (index == size) {
            insertLast(val);
            return;
        }

        Node temp = head;
        for (int i = 1; i < index; i++) {
            temp = temp.next;
        }
        Node node = new Node(val, temp.next);
        temp.next = node;
        size++;
    }

    public int deleteFirst() {
        int val = head.value;
        head = head.next;
        if (head == null) {
            tail = null;
        }
        size--;
        return val;
    }

    public int deleteLast() {
        if (size <= 1) {
            return deleteFirst();
        }
        Node secondLast = get(size - 2);
        int val = tail.value;
        tail = secondLast;
        tail.next = null;
        size--;
        return val;
    }

    public int delete(int index) {
        if (index == 0) {
            return deleteFirst();
        }
        if (index == size - 1) {
            return deleteLast();
        }
        Node prev = get(index - 1);
        int val = prev.next.value;
        prev.next = prev.next.next;
        size--;
        return val;
    }

    public Node get(int index) {
        Node node = head;
        for (int i = 0; i < index; i++) {
            node = node.next;
        }
        return node;
    }

    public void display() {
        Node temp = head;
        while (temp != null) {
            System.out.print(temp.value + " -> ");
            temp = temp.next;
        }
        System.out.println("END");
    }

    private class Node {
        private int value;
        private Node next;

        public Node(int value) {
            this.value = value;
        }

        public Node(int value, Node next) {
            this.value = value;
            this.next = next;
        }
    }

    public static LinkedListDemo merge(LinkedListDemo first, LinkedListDemo second) {
        Node f = first.head;
        Node s = second.head;

        LinkedListDemo result = new LinkedListDemo();
        while (f != null && s != null) {
            if (f.value < s.value) {
                result.insertLast(f.value);
                f = f.next;
            } else {
                result.insertLast(s.value);
                s = s.next;
            }
        }
        while (f != null) {
            result.insertLast(f.value);
            f = f.next;
        }
        while (s != null) {
            result.insertLast(s.value);
            s = s.next;
        }
        return result;
    }

    public static void main(String[] args) {
        LinkedListDemo first = new LinkedListDemo();
        LinkedListDemo second = new LinkedListDemo();

        first.insertLast(1);
        first.insertLast(3);
        first.insertLast(5);

        second.insertLast(1);
        second.insertLast(2);
        second.insertLast(9);
        second.insertLast(14);

        LinkedListDemo result = LinkedListDemo.merge(first, second);
        result.display();
    }
}